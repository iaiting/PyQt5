#include <stdio.h>

int main(int argc, char *argv) {
	int fahr;
	printf("华氏温度到摄氏温度转换表\n");

	for (fahr=0; fahr<=300; fahr+=20) {
		printf("%3d %6.1f\n", fahr, 5.0/9*(fahr-32));
	}

}
