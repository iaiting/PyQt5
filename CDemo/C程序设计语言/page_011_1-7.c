#include <stdio.h>

int main() {
	int eofv = EOF;
	printf("EOF Value:\n");
	printf("EOF 十进制值: %d\n",eofv);
	printf("EOF 八进制值: %o\n",eofv);
	printf("EOF 十六进制值: %x\n",eofv);
	
}
