/*******************************************************************************
 *
 * 将输入复制到输出; 版本1
 *
 ******************************************************************************/

#include <stdio.h>

int main(int argc, char *argv) {
	int c;
	c = getchar();
	while (c!=EOF) {
		putchar(c);
		c = getchar();
	}
	printf("程序正常结束\n");
}
